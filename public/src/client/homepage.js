import * as clientMain from "./client-main.js";
import {socket} from "./client-main.js";

/**
 * 
 * @param {*} maxPlayersField 
 * @param {number} maxPlayers 
 * @param {*} baseTimerDurationField 
 * @param {number} baseTimerDuration 
 * @param {*} startingLivesField 
 * @param {number} startingLives 
 * @param {HTMLElement} bonusAlphabetCheckboxesContainer 
 * @param {Array<string>} bonusAlphabet 
 */
async function preFillRoomRuleDefaults(
    maxPlayersField, maxPlayers,
    baseTimerDurationField, baseTimerDuration,
    startingLivesField, startingLives,
    bonusAlphabetCheckboxesContainer, bonusAlphabet
) {
    console.log(`trying to pre-fill room rule defaults`);

    // const response = await socket.timeout(10000).emitWithAck("get_room_rule_defaults");

    maxPlayersField.value = maxPlayers;
    baseTimerDurationField.value = baseTimerDuration;
    startingLivesField.value = startingLives;

    // reset bonus alphabet
    let aCharCode = "a".charCodeAt(0);

    for (let i = aCharCode; i < aCharCode + 26; i++) {
        let letter = String.fromCharCode(i);
        let letterCheckbox = document.getElementById(`${letter}-checkbox`);

        letterCheckbox.checked = bonusAlphabet.includes(letter.toUpperCase());
    }
}

function selectAllLetters(select = true) {
    let aCharCode = "a".charCodeAt(0);

    for (let i = aCharCode; i < aCharCode + 26; i++) {
        let letter = String.fromCharCode(i);    
        let letterCheckbox = document.getElementById(`${letter}-checkbox`);

        letterCheckbox.checked = select;
    }
}

function checkboxesToArray() {
    let arr = [];
    let aCharCode = "a".charCodeAt(0);

    for (let i = aCharCode; i < aCharCode + 26; i++) {
        let letter = String.fromCharCode(i);
        let letterCheckbox = document.getElementById(`${letter}-checkbox`);

        if (letterCheckbox.checked) {
            arr.push(letter.toUpperCase());
        }
    }

    return arr;
}

/**
 * 
 * @param {boolean} visible 
 */
function customWordsSettingsVisibility(customWordsSettingsContainer, visible) {
    if (visible) {
        clientMain.root.style.setProperty("--custom-words-settings-visibility", "flex");
    } else {
        clientMain.root.style.setProperty("--custom-words-settings-visibility", "none");
        customWordsField.value = "";
    }
}

function createRoom(
    username,
    maxPlayers,
    baseTimerDuration,
    startingLives,
    dictionarySelectionId,
    additionalWordsInput,
    usePresetDictionary,
    bonusAlphabet
) {
    console.log("trying to create room");

    if (clientMain.clientFlags.timesRoomCreated > 40) {
        alert("You have been making too many rooms and have been banned from doing so permanently (or until you refresh the page) (please don't refresh the page and continue spamming rooms");

    } else {
        socket.emit(
            "create_room", username, maxPlayers, baseTimerDuration, startingLives,
            dictionarySelectionId, additionalWordsInput, usePresetDictionary, bonusAlphabet
        );
    }

    clientMain.clientFlags.timesRoomCreated++;
}

function updateRoomsCount(roomsCountContainer, count) {
    roomsCountContainer.replaceChildren();

    let roomCountSpan = document.createElement("span");
    if (count !== 1) {
        roomCountSpan.textContent = `There are currently ${count} rooms active`;
    } else {
        roomCountSpan.textContent = `There is currently ${count} room active`;
    }
    roomsCountContainer.appendChild(roomCountSpan);

    console.log("updated rooms count");
}

