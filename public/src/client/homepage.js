import * as clientMain from "./client-main.js";
import {socket} from "./client-main.js";

async function preFillRoomRuleDefaults(
    maxPlayersField, maxPlayers,
    baseTimerDurationField, baseTimerDuration,
    startingLivesField, startingLives
) {
    console.log(`trying to pre-fill room rule defaults`)

    // const response = await socket.timeout(10000).emitWithAck("get_room_rule_defaults");

    maxPlayersField.value = maxPlayers;
    baseTimerDurationField.value = baseTimerDuration;
    startingLivesField.value = startingLives;
}

/**
 * 
 * @param {boolean} visible 
 */
function customWordsSettingsVisibility(customWordsSettingsContainer, visible) {
    if (visible) {
        clientMain.root.style.setProperty("--custom-words-settings-visibility", "flex");
    } else {
        clientMain.root.style.setProperty("--custom-words-settings-visibility", "none");
    }
}

function createRoom(
    maxPlayers,
    baseTimerDuration,
    startingLives
) {
    console.log("trying to create room");

    socket.emit("create_room", maxPlayers, baseTimerDuration, startingLives);
}

function updateRoomsCount(roomsCountContainer, count) {
    roomsCountContainer.replaceChildren();

    let roomCountSpan = document.createElement("span");
    if (count !== 1) {
        roomCountSpan.textContent = `There are currently ${count} rooms active`;
    } else {
        roomCountSpan.textContent = `There is currently ${count} room active`;
    }
    roomsCountContainer.appendChild(roomCountSpan);

    console.log("updated rooms count");
}

// get elements (homepage)
const usernameButton = document.getElementById("submit-username-button");
const usernameField = document.getElementById("username-field");

const maxPlayersField = document.getElementById("max-players-field");
const baseTimerDurationField = document.getElementById("base-timer-duration-field");
const startingLivesField = document.getElementById("starting-lives-field");
// contains room code once generated by user
const newlyGeneratedCodeContainer = document.getElementById("newly-generated-code"); 
const createRoomButton = document.getElementById("create-room-button");
const defaultRulesButton = document.getElementById("reset-rules-button");
const dictionarySelectDropdown = document.getElementById("dictionary-dropdown");
const customWordsSettings = document.getElementById("custom-words-settings");

const roomsCountContainer = document.getElementById("active-rooms-count");
const roomsList = document.getElementById("active-rooms-list");

// element listeners
usernameButton?.addEventListener("click", () => {
    clientMain.setUsername(usernameField, usernameField.value);
});
createRoomButton?.addEventListener("click", () => {
    createRoom(maxPlayersField.value, baseTimerDurationField.value, startingLivesField.value);
});
defaultRulesButton?.addEventListener("click", async () => {
    socket.emit("request_room_rule_defaults");
});
dictionarySelectDropdown?.addEventListener("change", (event) => {
    customWordsSettingsVisibility(customWordsSettings, event.target.value === "custom-dictionary");
});

// socket.io listeners
socket.on("update_rooms_count", (count) => {
    updateRoomsCount(roomsCountContainer, count);
});
socket.on("show_newly_generated_room", (generatedCode) => {
    clientMain.displayRoomCodeInfo(newlyGeneratedCodeContainer, generatedCode, true);
});
socket.on("pre_fill_room_rule_defaults", (maxPlayers, baseTimerDuration, startingLives) => {
    preFillRoomRuleDefaults(
        maxPlayersField, maxPlayers, 
        baseTimerDurationField, baseTimerDuration,
        startingLivesField, startingLives
    );
});

clientMain.preFillUsernameField(usernameField);